import Head from 'next/head'
import router, { useRouter } from 'next/router'
import { useEffect } from 'react'
import FlexViewContainer from '../../components/FlexViewContainer'
import EnterQueueID from '../../views/EnterQueueID'

export default function JoinQueue() {

  // TODO: Allow slug to be used so that sharing is more elegant :)

  const router = useRouter();

  useEffect(() => {
    let userName = localStorage.getItem('QueuedUpUserName');
    let userId = localStorage.getItem('QueuedUpUserId');

    if (!userName || !userId) {
      router.push('/signin');
    };
  }, [router]);

  return (
    <FlexViewContainer>
      <Head>
        <title>Join a Queue!</title>
        <meta name="Webservice for virtual queues" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <EnterQueueID />

    </FlexViewContainer>
  )
}
